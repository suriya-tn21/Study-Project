with st.expander("Measures to Correct Deficient Demand"):
    st.write("""
1. Increase in Government Spending: Govt increasing expenditure on public works provide additional income to people. This will help increase aggregate demand.
2. Decrease in Taxes: Reducing taxes raises purchasing power of people. Increase in disposable income leads people to spend more on consumption and investment and will help increase aggregate demand.
3. Decrease in Bank Rate: It's the rate at which central bank lends money to commercial banks to meet their long term needs. Reducing bank rate will expand credit. It leads to fall in market rate of interest which induces people to borrow. Ultimately leading to increase in AD.
4. Decrease in Repo Rate: It's the rate at which central bank lends money to commercial banks to meet their short term needs. Reducing repo rate will expand credit. It leads to fall in market rate of interest which induces people to borrow. Ultimately leading to increase in AD.
5. Decrease in Reverse Repo Rate: It's the rate at which commercial banks can deposit theiry surplus funds with Central Bank for a short period of time. Reducing the rate will discourage commercial banks to deposit their surplus funds which raises credit creating power which'll increase consumption and investment expenditure, leading to rise in AD.
6. Purchase of Securities: It increases money supply and enhances purchasing power capacity and increases level of AD.
7. Decrease in Legal Reserve Ratio: Decrease in CRR and SLR helps raise availability of credit. It increases amount of cash resources of commercial banks and enhances their credit creating power.
8. Decrease in Margin Requirements: Reducing Margin Requirements will enhance credit creating power of banks. More loans can be issued against the same amount of security.
9. Moral Suasion: Central bank advises, requests, persuades the commercial banks to encourage credit. Helps raise availability of credit and AD.
10. Selective Credit Controls: Central Bank withdraws rationing of credit and makes efforts to encourage credit
""")
